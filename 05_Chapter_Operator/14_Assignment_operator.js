// Assignment operators
let a = 10; // Assigns the value 10 to variable a
let b = 5;  // Assigns the value 5 to variable b
console.log(a); // Output: 10   
console.log(b); // Output: 5

// Addition assignment operator
a += b; // Equivalent to a = a + b
console.log(a); // Output: 15           



let x1 = 10; // Assigns the value 10 to variable x
x1 += 5; // Equivalent to x = x + 5
console.log(x1); // Output: 15

x1 -= 3; // Equivalent to x = x - 3
console.log(x1); // Output: 12

x1 *= 2; // Equivalent to x = x * 2
console.log(x1); // Output: 24

x1 /= 4;    // Equivalent to x = x / 4
console.log(x1);    // Output: 6